import Head from 'next/head';
import Image from 'next/image';
import moment from 'moment';
import { useState, useEffect } from 'react';
import styles from '../styles/Home.module.css';
import useDebounce from '../hooks/useDebounce';
export default function Home() {
  const [jobs, setJobs] = useState([]);
  const [search, setSearch] = useState(null);
  const debouncedSearch = useDebounce(search, 500);
  moment.locale();

  useEffect(() => {
    async function fetchJobs() {
      const data = await fetch(
        `https://www.arbeitnow.com/api/job-board-api`
      ).then((res) => res.json());
      setJobs(data);
    }
    fetchJobs();
  }, [debouncedSearch]);
  const date = moment(jobs.data?.created_at).format('lll');
  return (
    <div className={styles.container}>
      <Head>
        <title>Zerezes Code Challenge</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <main className={styles.main}>
        <h1>oi</h1>
        <input
          type="search"
          placeholder="Pesquisar"
          onChange={(e) => setSearch(e.target.value)}
        />
        {jobs.data
          ?.filter((value) => {
            if (debouncedSearch === ``) {
              return value;
            } else if (debouncedSearch === null) {
              return value;
            } else if (
              value.title?.toLowerCase().startsWith(search?.toLowerCase())
            ) {
              return value;
            }
          })
          .map((job) => {
            return (
              <div key={job.slug}>
                <div className="title-container">
                  <h4>{job.title}</h4>
                  <p>{date}</p>
                </div>
                <p>{job.company_name}</p>
                <div>icone {job.remote ? 'Remoto' : `${job.location}`}</div>
                <a href={job.url}>clique aqui</a>
              </div>
            );
          })}
      </main>
    </div>
  );
}
